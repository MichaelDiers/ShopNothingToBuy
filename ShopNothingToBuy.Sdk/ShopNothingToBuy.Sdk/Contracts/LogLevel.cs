namespace ShopNothingToBuy.Sdk.Contracts
{
	/// <summary>
	///   Specifies the log level of a log entry.
	/// </summary>
	public enum LogLevel
	{
		/// <summary>
		///   Undefined log level.
		/// </summary>
		None = 0,

		/// <summary>
		///   Log level debug.
		/// </summary>
		Debug = 1,

		/// <summary>
		///   Log level info.
		/// </summary>
		Info = 2,

		/// <summary>
		///   Log  level warning.
		/// </summary>
		Warning = 3,

		/// <summary>
		///   Log level error.
		/// </summary>
		Error = 4
	}
}